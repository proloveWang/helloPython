# 计算1~100之间偶数的累积和（包含1和100）
num = 1 #控制次数的
sum = 0 #累加 0 1 3 6.....  5050
while num <= 100:
    # 判断数字是否为偶数，如果为偶数，就累加，不是偶数放弃
    if num % 2 == 0:
        sum += num
    num += 1
print("累加的结果为"+str(sum))
